#' Render points at ordinates
#' 

#' \code{geom_*_point} renders symbols at the positions of the subjects or
#' vectors.
#' @template ggbiplot-layers
#' @template matrix-param

#' @section Aesthetics:
#' \code{geom_*_point} understands the following aesthetics (required aesthetics
#' are in bold):
#' \itemize{
#'   \item \strong{\code{x}}
#'   \item \strong{\code{y}}
#'   \item \code{alpha}
#'   \item \code{colour}
#'   \item \code{fill}
#'   \item \code{linetype}
#'   \item \code{size}
#'   \item \code{group}
#' }
#' 

#' @name ggbiplot-point
#' @import ggplot2
#' @inheritParams ggplot2::layer
#' @template layer-params
#' @example inst/examples/ex-bibble-lm.r

#' @rdname ggbiplot-point
#' @export
geom_u_point <- function(
  mapping = NULL, data = NULL, position = "identity",
  ...,
  na.rm = FALSE,
  show.legend = NA, inherit.aes = TRUE
) {
  layer(
    data = data,
    mapping = mapping,
    stat = "u",
    geom = GeomPoint,
    position = position,
    show.legend = show.legend,
    inherit.aes = inherit.aes,
    params = list(
      na.rm = na.rm,
      ...
    )
  )
}

